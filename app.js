function watchlistMatches(rule) {
  const keyword = rule.keyword.trim().toLowerCase();
  return deals.filter(deal => {
    const math = dealMath(deal);
    const haystack = `${deal.title} ${deal.category} ${deal.source} ${deal.ground}`.toLowerCase();
    const keywordMatch = !keyword || haystack.includes(keyword);
    const groundMatch = rule.ground === "Any" || deal.ground === rule.ground;
    return keywordMatch && groundMatch && math.profit >= rule.profit && deal.distance <= rule.radius;
  });
}

