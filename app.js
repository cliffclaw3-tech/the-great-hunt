function challengeStageForFind(find) {
  if ((find.betaStatus || "") === "bought") return "bought";
  if ((find.betaStatus || "") === "passed") return "passed";
  if ((find.betaStatus || "") === "contacted") return "contacted";
  return "watching";
}

