function includedReviewComps(deal) {
  if (!deal.compReview) return [];
  return [...(deal.compReview.accepted || []), ...(deal.compReview.rejected || [])]
    .filter(comp => comp.included && Number(comp.total || comp.price) > 0);
}

function recalculateDealFromReview(deal) {
  const included = includedReviewComps(deal);
  if (!included.length) return deal;

  const totals = included.map(comp => Number(comp.total || comp.price || 0)).filter(Boolean);
  const low = percentile(totals, 0.15);
  const mid = median(totals);
  const high = percentile(totals, 0.85);
  const confidence = Math.round(average(included.map(comp => Number(comp.confidence || 0))));
  const details = conditionDetails(deal.conditionKey || "clean");
  const rawFastSale = Math.round(low * 0.97);
  const adjustedFastSale = deal.allowBelowAsk
    ? Math.round(rawFastSale * details.multiplier)
    : Math.max(Number(deal.ask || 0) + 20, Math.round(rawFastSale * details.multiplier));

  return {
    ...deal,
    fastSale: adjustedFastSale,
    suggestedAsk: Math.round(mid),
    priceLow: Math.round(low),
    priceMedian: Math.round(mid),
    priceHigh: Math.round(high),
    comps: included.length,
    compRange: `${money(low * details.multiplier)}-${money(high * details.multiplier)}`,
    confidence,
    compLinks: included.slice(0, 6).map(comp => [
      `${comp.condition || "Listing"}: ${comp.title}`,
      comp.url || "#"
    ]),
    explanation: `Recalculated from ${included.length} manually included comps. Range uses roughly the 15th/50th/85th percentile of listing + shipping totals (${money(low)} / ${money(mid)} / ${money(high)}).`
  };
}

function recalculateLookupFromReview() {
  if (!currentLookupDeal?.compReview) return;

  currentLookupDeal = recalculateDealFromReview(currentLookupDeal);
}

