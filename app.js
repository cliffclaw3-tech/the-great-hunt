async function scanRadar(event) {
  event?.preventDefault();
  const results = document.querySelector("#radarResults");
  const payload = {
    zip: document.querySelector("#radarZip").value.trim(),
    radius: Number(document.querySelector("#radarRadius").value || 0),
    keywords: document.querySelector("#radarKeywords").value.trim(),
    sources: document.querySelector("#radarSources").value.trim()
  };
  logActivityEvent({
    type: "radar-search",
    query: payload.keywords,
    distance: payload.radius,
    source: payload.sources ? "Submitted URLs" : "No submitted URLs",
    hasPhoto: false
  });

  results.innerHTML = `
    <article class="empty-state">
      <h3>Scanning sources</h3>
      <p>Looking for named brands, collectible categories, sale previews, and research-worthy clues.</p>
    </article>
  `;

  try {
    radarScan = await apiRequest("/api/radar/scan", {
      method: "POST",
      body: JSON.stringify(payload)
    });
  } catch (error) {
    logActivityEvent({
      type: "radar-failed",
      query: payload.keywords,
      distance: payload.radius,
      source: payload.sources ? "Submitted URLs" : "No submitted URLs",
      message: error.message || "Radar scan failed.",
      success: false
    });
    radarScan = {
      zip: payload.zip,
      radius: payload.radius,
      scanned: 0,
      liveSources: 0,
      error: error.message || "Scan could not run.",
      leads: [
        {
          id: "scan-error",
          title: "Radar could not reach the scanner",
          category: "Scanner issue",
          sourceType: "Local scanner",
          sourceName: "The Great Hunt",
          sourceUrl: "",
          distance: 0,
          score: 0,
          signals: ["try again"],
          keywordHits: [],
          summary: "The scan request did not complete. Make sure the app is opened through the live local server, then try again.",
          lookupItem: "",
          suggestedGround: "Estate sale",
          nextSteps: ["Refresh the app", "Try a public estate sale or auction preview URL", "If the source blocks scanning, open it manually and copy item names into Lookup"]
        }
      ]
    };
  }

  renderRadarResults();
  logActivityEvent({
    type: "radar-result",
    query: payload.keywords,
    distance: payload.radius,
    source: radarScan.liveSources ? "Submitted sources" : "Prototype or fallback sources",
    comps: radarScan.leads?.length || 0,
    success: !radarScan.error
  });
}

