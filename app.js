function endTour(markSeen = true) {
  activeTourTarget?.classList.remove("tour-target");
  activeTourTarget = null;
  tourOverlay?.remove();
  tourOverlay = null;
  closeFilters();
  if (markSeen) localStorage.setItem(tourSeenKey, "yes");
}

function cancelPendingTour() {
  if (tourStartTimer) {
    window.clearTimeout(tourStartTimer);
    tourStartTimer = null;
  }
  if (tourOverlay) endTour(false);
}

function renderTourStep(steps) {
  const step = steps[activeTourIndex];
  if (!step) {
    endTour();
    return;
  }

  activeTourTarget?.classList.remove("tour-target");
  step.before?.();
  activeTourTarget = document.querySelector(step.target);
  if (!activeTourTarget) {
    activeTourIndex += 1;
    renderTourStep(steps);
    return;
  }

  activeTourTarget.classList.add("tour-target");
  activeTourTarget.scrollIntoView({ behavior: "smooth", block: "center" });

  tourOverlay.innerHTML = `
    <div class="tour-backdrop" aria-hidden="true"></div>
    <section class="tour-card" role="dialog" aria-live="polite" aria-label="App walkthrough">
      <div>
        <p class="eyebrow">Quick tour ${activeTourIndex + 1} of ${steps.length}</p>
        <h2>${escapeHtml(step.title)}</h2>
        <p>${escapeHtml(step.copy)}</p>
      </div>
      <div class="tour-actions">
        <button class="secondary-action" data-tour-skip type="button">Skip</button>
        <button class="primary-action" data-tour-next type="button">${activeTourIndex === steps.length - 1 ? "Done" : "OK"}</button>
      </div>
    </section>
  `;
}

function startTourIfNeeded() {
  const forceTour = new URLSearchParams(window.location.search).get("tour") === "1";
  if (!forceTour && localStorage.getItem(tourSeenKey)) return;
  const steps = visibleTourSteps();
  if (!steps.length) return;

  activeTourIndex = 0;
  tourOverlay = document.createElement("div");
  tourOverlay.className = "tour-overlay";
  document.body.append(tourOverlay);

  tourOverlay.addEventListener("click", event => {
    if (event.target.closest("[data-tour-skip]")) {
      endTour();
      return;
    }

    if (event.target.closest("[data-tour-next]")) {
      steps[activeTourIndex]?.after?.();
      activeTourIndex += 1;
      renderTourStep(steps);
    }
  });

  renderTourStep(steps);
}

