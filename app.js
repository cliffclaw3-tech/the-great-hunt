function dealVerdict(deal) {
  const status = String(deal.valuationStatus || "").toLowerCase();
  const method = String(deal.valuationMethod || "").toLowerCase();
  if (deal.researchMode || status === "baseline" || method.includes("fallback") || method.includes("baseline")) {
    return {
      label: "Research mode",
      tone: "maybe",
      copy: "No reliable price comps came back yet. The app found next places to check and the details needed to tighten the search."
    };
  }

  if (deal.lowValueSignal) {
    return {
      label: "Weak resale signal",
      tone: "skip",
      copy: "This looks like a common everyday item with no obvious resale signal. Only keep researching if there is something special about it."
    };
  }

  const math = dealMath(deal);
  if (Number(deal.ask || 0) > 0 && Number(deal.fastSale || 0) < Number(deal.ask || 0)) {
    return {
      label: "Skip — sells below ask",
      tone: "skip",
      copy: "Accepted comps point below the seller's ask. Do not let the asking price anchor the value."
    };
  }

  if (math.profit >= 250 && math.margin >= 20 && Number(deal.comps || 0) >= 4) {
    return {
      label: "Worth a closer look",
      tone: "good",
      copy: "The price leaves room after a fast-sale estimate. Check condition before you drive or message."
    };
  }

  if (math.profit >= 75 && math.margin >= 10) {
    return {
      label: "Maybe",
      tone: "maybe",
      copy: "There may be room here, but condition, exact model, and fees matter."
    };
  }

  return {
    label: "Weak spread signal",
    tone: "skip",
    copy: "The numbers do not show much room right now. Keep researching only if you know something special."
  };
}

function dealActionPlan(deal) {
  if (deal.lowValueSignal) {
    return {
      move: "Needs a special signal",
      offer: "No guardrail",
      caution: "Look for vintage branding, a limited promotion, misprint, unopened packaging, or a complete set."
    };
  }

  const math = dealMath(deal);
  const offerCeiling = Math.max(1, Math.round(Number(deal.fastSale || 0) * 0.58));

  if (math.profit >= 250 && math.margin >= 20) {
    return {
      move: "Verify condition and model details",
      offer: money(Math.min(offerCeiling, Number(deal.ask || offerCeiling))),
      caution: "Verify exact model and damage before paying."
    };
  }

  if (math.profit >= 75 && math.margin >= 10) {
    return {
      move: "Needs low-friction pickup",
      offer: money(Math.min(offerCeiling, Math.max(1, Math.round(Number(deal.ask || 0) * 0.85)))),
      caution: "Small mistakes could erase the spread."
    };
  }

  return {
    move: "Needs niche knowledge",
    offer: money(Math.max(1, Math.round(Number(deal.fastSale || 0) * 0.45))),
    caution: "The numbers do not leave much room."
  };
}

