function loadCallbackState() {
  try {
    const state = JSON.parse(localStorage.getItem(callbackStateKey) || "{}");
    return state && typeof state === "object" ? state : {};
  } catch {
    return {};
  }
}

function saveCallbackState(patch) {
  const state = {
    ...loadCallbackState(),
    ...patch
  };
  localStorage.setItem(callbackStateKey, JSON.stringify(state));
  return state;
}

function callbackStoryFor(date = new Date()) {
  const dayIndex = Math.floor(date.getTime() / 86400000);
  return callbackStories[dayIndex % callbackStories.length];
}

function callbackMessage(story = callbackStoryFor()) {
  return `${story.name} found ${story.item} at ${story.place} in ${story.year}. It sold for ${story.soldFor}. What will you find this weekend?`;
}

function notificationAvailable() {
  return "Notification" in window && location.protocol !== "file:";
}

async function requestCallbackNotifications() {
  if (!notificationAvailable()) return "unavailable";
  if (Notification.permission === "granted") {
    saveCallbackState({ notificationsEnabledAt: new Date().toISOString() });
    renderCallbackPrompt(true);
    return "granted";
  }

  if (Notification.permission === "denied") return "denied";

  try {
    const permission = await Notification.requestPermission();
    if (permission === "granted") {
      saveCallbackState({ notificationsEnabledAt: new Date().toISOString() });
      showCallbackNotification(callbackMessage());
    }
    renderCallbackPrompt(true);
    return permission;
  } catch {
    return "unavailable";
  }
}

function showCallbackNotification(message) {
  if (!notificationAvailable() || Notification.permission !== "granted") return;
  navigator.serviceWorker?.ready
    .then(registration => registration.showNotification("The Great Hunt", {
      body: message,
      icon: "/assets/app-icon-192.png",
      badge: "/assets/app-icon-192.png",
      tag: "great-hunt-callback",
      data: { url: "/?callback=1" }
    }))
    .catch(() => {
      try {
        new Notification("The Great Hunt", { body: message, icon: "/assets/app-icon-192.png" });
      } catch {}
    });
}

function shouldShowCallbackPrompt(state, now = new Date()) {
  const lastSeen = state.lastSeenAt ? new Date(state.lastSeenAt) : null;
  const lastPrompt = state.lastPromptAt ? new Date(state.lastPromptAt) : null;
  if (!lastSeen || Number.isNaN(lastSeen.getTime())) return false;

  const quietDays = (now.getTime() - lastSeen.getTime()) / 86400000;
  const promptedToday = lastPrompt && (now.getTime() - lastPrompt.getTime()) < 86400000;
  return quietDays >= 3 && !promptedToday;
}

function renderCallbackPrompt(force = false) {
  const prompt = document.querySelector("#callbackPrompt");
  if (!prompt) return;

  const state = loadCallbackState();
  if (!force && !shouldShowCallbackPrompt(state)) {
    prompt.hidden = true;
    prompt.innerHTML = "";
    return;
  }

  const story = callbackStoryFor();
  const message = callbackMessage(story);
  const canAsk = notificationAvailable() && Notification.permission === "default";
  const enabled = notificationAvailable() && Notification.permission === "granted";
  prompt.hidden = false;
  prompt.innerHTML = `
    <div>
      <p class="eyebrow">Weekend callback</p>
      <h2>${escapeHtml(story.name)} found treasure. Your turn?</h2>
      <p>${escapeHtml(message)}</p>
    </div>
    <div class="callback-actions">
      <button class="primary-action" data-callback-hunt type="button">Look for treasure</button>
      ${canAsk ? `<button class="secondary-action" data-callback-enable type="button">Enable reminders</button>` : ""}
      ${enabled ? `<span class="callback-enabled">Reminders enabled</span>` : ""}
      <button class="secondary-action" data-callback-dismiss type="button">Later</button>
    </div>
  `;
}

function markAppSeenForCallback() {
  const now = new Date().toISOString();
  saveCallbackState({ lastSeenAt: now });
}

function setupCallbackFeature() {
  const state = loadCallbackState();
  renderCallbackPrompt();
  if (shouldShowCallbackPrompt(state)) {
    const message = callbackMessage();
    showCallbackNotification(message);
    saveCallbackState({ lastPromptAt: new Date().toISOString() });
  }
  window.setTimeout(markAppSeenForCallback, 1500);
}

