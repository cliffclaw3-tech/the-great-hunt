function speechRecognitionClass() {
  return window.SpeechRecognition || window.webkitSpeechRecognition || null;
}

function setVoiceStatus(control, message) {
  const label = control.closest("label");
  label?.querySelector(".voice-status")?.replaceChildren(document.createTextNode(message));
}

function applyVoiceTranscript(control, transcript) {
  const cleanTranscript = transcript.trim();
  if (!cleanTranscript) return;

  if (control.tagName === "TEXTAREA") {
    const spacer = control.value.trim() ? "\n" : "";
    control.value = `${control.value.trim()}${spacer}${cleanTranscript}`;
  } else {
    control.value = cleanTranscript;
  }

  control.dispatchEvent(new Event("input", { bubbles: true }));
  control.dispatchEvent(new Event("change", { bubbles: true }));
}

function startVoiceInput(control, button, prompt) {
  const Recognition = speechRecognitionClass();
  if (!Recognition) {
    control.focus();
    setVoiceStatus(control, "Use the microphone on your phone keyboard for this field.");
    return;
  }

  const recognition = new Recognition();
  recognition.lang = "en-US";
  recognition.continuous = false;
  recognition.interimResults = true;

  let finalTranscript = "";
  const originalText = button.textContent;
  button.classList.add("is-listening");
  button.textContent = "Listening";
  setVoiceStatus(control, prompt);

  recognition.onresult = event => {
    let interimTranscript = "";
    for (let index = event.resultIndex; index < event.results.length; index += 1) {
      const transcript = event.results[index][0].transcript;
      if (event.results[index].isFinal) {
        finalTranscript += transcript;
      } else {
        interimTranscript += transcript;
      }
    }
    setVoiceStatus(control, interimTranscript ? `Heard: ${interimTranscript.trim()}` : "Listening...");
  };

  recognition.onerror = () => {
    setVoiceStatus(control, "I could not hear that. Try again or use the keyboard mic.");
  };

  recognition.onend = () => {
    button.classList.remove("is-listening");
    button.textContent = originalText;
    if (finalTranscript.trim()) {
      applyVoiceTranscript(control, finalTranscript);
      setVoiceStatus(control, "Added. You can edit it before searching.");
    } else {
      setVoiceStatus(control, "Tap Speak and try again, or use the phone keyboard mic.");
    }
  };

  try {
    recognition.start();
  } catch {
    button.classList.remove("is-listening");
    button.textContent = originalText;
    setVoiceStatus(control, "Voice input is not available right now.");
  }
}

function setupVoiceInputs() {
  voiceFields.forEach(([selector, prompt]) => {
    const control = document.querySelector(selector);
    if (!control || control.dataset.voiceReady) return;

    const label = control.closest("label");
    if (!label) return;

    const row = document.createElement("div");
    row.className = "voice-field-row";
    control.replaceWith(row);
    row.append(control);

    const button = document.createElement("button");
    button.className = "voice-action";
    button.type = "button";
    button.textContent = "Speak";
    button.setAttribute("aria-label", `${prompt} by voice`);
    row.append(button);

    const status = document.createElement("span");
    status.className = "voice-status";
    status.setAttribute("role", "status");
    status.textContent = speechRecognitionClass()
      ? "Tap Speak and say it out loud."
      : "You can use the microphone on your phone keyboard.";
    label.append(status);

    button.addEventListener("click", () => startVoiceInput(control, button, prompt));
    control.dataset.voiceReady = "true";
  });
}

