function readPhotoFile(file, fallbackName) {
  return new Promise((resolve, reject) => {
    if (!file || !file.type.startsWith("image/")) {
      reject(new Error("Choose a photo file."));
      return;
    }

    const reader = new FileReader();
    reader.addEventListener("load", () => {
      const img = new Image();
      img.addEventListener("load", () => {
        const maxSide = 1200;
        const scale = Math.min(1, maxSide / Math.max(img.width, img.height));
        const width = Math.max(1, Math.round(img.width * scale));
        const height = Math.max(1, Math.round(img.height * scale));
        const canvas = document.createElement("canvas");
        canvas.width = width;
        canvas.height = height;
        canvas.getContext("2d").drawImage(img, 0, 0, width, height);
        resolve({
          src: canvas.toDataURL("image/jpeg", 0.82),
          alt: `${fallbackName || itemNameFromPhoto(file) || "Estate sale find"} photo`,
          name: file.name,
          position: "center",
          isUserPhoto: true
        });
      });
      img.addEventListener("error", () => reject(new Error("Photo could not be loaded.")));
      img.src = reader.result;
    });
    reader.addEventListener("error", () => reject(new Error("Photo could not be read.")));
    reader.readAsDataURL(file);
  });
}

