function applyConditionToDeal(deal, condition) {
  const details = conditionDetails(condition);
  const baseFastSale = Number(deal.fastSale || 0);
  const ask = Number(deal.ask || 0);
  const adjustedFastSale = deal.allowBelowAsk
    ? Math.round(baseFastSale * details.multiplier)
    : Math.max(ask + 10, Math.round(baseFastSale * details.multiplier));
  return {
    ...deal,
    condition: details.label,
    conditionKey: condition || "clean",
    conditionMultiplier: details.multiplier,
    conditionNote: details.note,
    conditionHints: conditionHints(deal.category),
    unadjustedFastSale: Math.round(baseFastSale),
    fastSale: adjustedFastSale,
    explanation: `${deal.explanation || ""} Condition selected: ${details.label}. ${details.note}`.trim()
  };
}

function qualifies(deal) {
  const t = thresholds();
  const math = dealMath(deal);
  return math.profit >= t.profit && math.margin >= t.margin && deal.distance <= t.radius;
}

function dataQualitySummary(deal) {
  const source = String(deal.source || "");
  const evidenceStatus = String(deal.marketEvidence?.status || "");
  const accepted = deal.compReview?.accepted || [];
  const rejected = deal.compReview?.rejected || [];
  const photoChecked = accepted.filter(item => item.photoMatchScore !== null && item.photoMatchScore !== undefined);
  const strongPhotoMatches = photoChecked.filter(item => Number(item.photoMatchScore || 0) >= 65).length;
  const soldSource = /sold|insights/i.test(source);
  const liveSource = /browse|reverb|active|api/i.test(source) && !soldSource;
  const fallbackSource = /fallback/i.test(source);
  const exactishMatches = accepted.filter(item => Number(item.confidence || 0) >= 75).length;
  const notes = [];

  let label = "Use with care";
  let tone = "maybe";
  if (evidenceStatus === "asking-prices-only") {
    label = "Asking prices only";
    tone = deal.comps >= 3 ? "maybe" : "skip";
  } else if (evidenceStatus === "research-links-only" || fallbackSource || deal.comps < 3) {
    label = "Needs more proof";
    tone = "skip";
  } else if (soldSource && exactishMatches >= 3) {
    label = "Stronger estimate";
    tone = "good";
  } else if (liveSource && exactishMatches >= 3) {
    label = "Good active comps";
    tone = "maybe";
  }

  if (soldSource) {
    notes.push("Uses sold/completed comp data.");
  } else if (evidenceStatus === "asking-prices-only") {
    notes.push("No clean sold comps found; this uses comparable asking prices.");
  } else if (evidenceStatus === "research-links-only") {
    notes.push("No close comps found; use the research links first.");
  } else if (liveSource) {
    notes.push("Uses active listings, not guaranteed sale prices.");
  } else if (fallbackSource) {
    notes.push("Fallback estimate, not live market pricing.");
  }

  if (photoChecked.length) {
    notes.push(`${strongPhotoMatches}/${photoChecked.length} comp photos looked close.`);
  } else if (deal.photoReview || deal.photo?.src?.startsWith("data:image/")) {
    notes.push("User photo saved; comp photos were not all available to compare.");
  } else {
    notes.push("Add a photo to compare the item against comp pictures.");
  }

  if (rejected.length) {
    notes.push(`${rejected.length} weak comps filtered out.`);
  }

  if (deal.comps < 3) {
    notes.push("Too few comps for a confident number.");
  } else {
    notes.push(`${deal.comps} comps used in the range.`);
  }

  return { label, tone, notes: notes.slice(0, 4) };
}

function marketEvidenceSummary(deal, quality) {
  const evidence = deal.marketEvidence || {};
  const status = String(evidence.status || "");
  const usesSoldComps = /sold|insights/i.test(String(deal.source || "")) || status === "sold-comps";
  const askingOnly = status === "asking-prices-only";
  const researchOnly = status === "research-links-only" || (deal.researchMode && !Number(deal.comps || 0));
  const rejectedCount = (deal.compReview?.rejected || []).length;
  const missingProof = deal.valuationProof?.missing?.[0] || verificationChecklist(deal)[0] || "Verify exact identifiers";
  let label = "Active market estimate";
  let detail = "Compare exact identifiers, condition, and photos before trusting the number.";
  let tone = quality.tone || "maybe";

  if (usesSoldComps) {
    label = "Sold comps";
    detail = evidence.detail || "Sold/completed comp evidence is connected for this item.";
    tone = "good";
  } else if (askingOnly) {
    label = evidence.label || "No clean sold comps found";
    detail = evidence.detail || "Comparable active listings are asking prices, not proof buyers paid that amount.";
    tone = Number(deal.comps || 0) >= 3 ? "maybe" : "skip";
  } else if (researchOnly) {
    label = evidence.label || "Research mode";
    detail = evidence.detail || "No reliable comp set survived filtering. Add identifiers before trusting a price.";
    tone = "skip";
  }

  return {
    label,
    detail,
    tone,
    rejectedCount,
    missingProof,
    nextAction: researchOnly
      ? "Add proof"
      : askingOnly
        ? "Check solds"
        : usesSoldComps
          ? "Verify match"
          : "Compare photos"
  };
}

function trustStateFor(deal, quality = dataQualitySummary(deal)) {
  if (deal.valuationStatus === "no-value-signal" || deal.lowValueSignal) {
    return {
      label: "Skip",
      tone: "skip",
      detail: "No strong resale signal yet. Only keep going if you see a rare mark, sealed set, error, or another real scarcity clue."
    };
  }

  if (deal.researchMode || deal.valuationStatus === "baseline" || quality.tone === "skip") {
    return {
      label: "Research only",
      tone: "maybe",
      detail: "Do not buy from this number alone. Add proof, inspect exact identifiers, and compare tighter comps first."
    };
  }

  if (deal.marketEvidence?.status === "asking-prices-only") {
    return {
      label: "Asking-price value",
      tone: "maybe",
      detail: "Useful for field judgment, but not sold proof. Confirm demand with sold comps before paying up."
    };
  }

  return {
    label: "Market value",
    tone: "good",
    detail: "Enough comp evidence survived filtering to use as a field estimate. Still verify condition, authenticity, and costs."
  };
}

function quickFlipAssessment(deal) {
  const costs = dealCostBreakdown(deal);
  const quality = dataQualitySummary(deal);
  const trust = trustStateFor(deal, quality);
  const rejected = deal.compReview?.rejected || [];
  const accepted = deal.compReview?.accepted || [];
  const rejectedReasons = rejected.map(item => item.reason).filter(Boolean);
  const blockerPatterns = [
    /part|accessory|incomplete|repair|broken|damaged|read description|disclosed/i,
    /reproduction|replica|fake|counterfeit|authenticity/i,
    /bundle|lot|set/i
  ];
  const trapReasons = rejectedReasons.filter(reason => blockerPatterns.some(pattern => pattern.test(reason)));
  const blockers = [];
  const checks = [];

  if (trust.tone === "skip" || deal.lowValueSignal) blockers.push("No reliable resale signal yet");
  if (deal.valuationStatus === "baseline" || deal.researchMode) blockers.push("Starter estimate only");
  if (deal.marketEvidence?.status === "asking-prices-only") blockers.push("Asking prices only");
  if (Number(deal.comps || 0) < 4) blockers.push("Too few clean comps");
  if (costs.net < 15) blockers.push("Net after costs is too thin");
  if (trapReasons.length) blockers.push(`${trapReasons.length} trap comp${trapReasons.length === 1 ? "" : "s"} filtered`);
  if (accepted.length && accepted.length < 3) blockers.push("Not enough accepted matches");

  if (deal.category === "Sports cards" || deal.category === "Coins" || deal.category === "Watches") {
    checks.push("Verify authenticity before counting this as a buy.");
  }
  if (deal.category === "Cameras" || deal.category === "Vintage audio" || deal.category === "Retail arbitrage") {
    checks.push("Confirm tested/working status and included accessories.");
  }
  if (deal.category === "Books" || deal.category === "Pens") {
    checks.push("Confirm exact edition/model and condition details.");
  }
  if (costs.net < 40) checks.push("Paper trade only unless seller accepts a lower buy price.");
  if (!/sold|insights/i.test(String(deal.source || ""))) checks.push("Use sold comps before real money changes hands.");

  const score = Math.max(0, Math.min(100,
    35
    + Math.min(25, Number(deal.comps || 0) * 3)
    + Math.min(20, Math.max(0, costs.net))
    + (trust.tone === "good" ? 15 : trust.tone === "maybe" ? 6 : -20)
    - blockers.length * 12
  ));
  const tone = blockers.length >= 3 || costs.net < 15 || trust.tone === "skip"
    ? "skip"
    : blockers.length || costs.net < 40
      ? "maybe"
      : "good";
  const label = tone === "good"
    ? "Quick flip ready"
    : tone === "maybe"
      ? "Paper trade only"
      : "Block from challenge";
  const detail = tone === "good"
    ? "Strong enough for the challenge queue after exact item and condition are verified."
    : tone === "maybe"
      ? "Useful for paper testing, but not clean enough to treat as a real buy signal yet."
      : "Do not count this as a quick-flip lead until the blockers are cleared.";

  return {
    label,
    tone,
    score: Math.round(score),
    detail,
    net: costs.net,
    blockers: blockers.slice(0, 4),
    checks: checks.slice(0, 4)
  };
}

