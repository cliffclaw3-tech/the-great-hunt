function dealCostBreakdown(deal) {
  if (deal.lowValueSignal) {
    return {
      sellingFees: 0,
      packing: 0,
      pickup: 0,
      repairReserve: 0,
      net: Number(deal.fastSale || 0) - Number(deal.ask || 0),
      tone: "skip",
      note: "No meaningful resale spread unless a special collector signal is verified."
    };
  }

  const fastSale = Number(deal.fastSale || 0);
  const ask = Number(deal.ask || 0);
  const categoryPacking = {
    "Vintage audio": 35,
    Instruments: 30,
    "Retail arbitrage": 12,
    Books: 7,
    Pens: 6,
    Watches: 10,
    Knives: 8,
    Tools: 18,
    Cameras: 16
  };
  const conditionReserve = {
    rough: Math.max(25, Math.round(fastSale * 0.18)),
    working: Math.round(fastSale * 0.08),
    clean: Math.round(fastSale * 0.04),
    restored: 0,
    sealed: 0
  };
  const sellingFees = Math.round(fastSale * 0.13);
  const packing = categoryPacking[deal.category] ?? 12;
  const pickup = Math.round(Number(deal.distance || 0) * 1.1);
  const repairReserve = conditionReserve[deal.conditionKey || "clean"] ?? Math.round(fastSale * 0.06);
  const net = Math.round(fastSale - ask - sellingFees - packing - pickup - repairReserve);
  const tone = net >= 150 ? "good" : net >= 40 ? "maybe" : "skip";
  const note = tone === "good"
    ? "Still leaves room after ordinary selling friction."
    : tone === "maybe"
      ? "Possible, but one surprise can erase the win."
      : "Costs likely eat most of the spread.";

  return { sellingFees, packing, pickup, repairReserve, net, tone, note };
}

function clearanceTagSummary(deal) {
  const tag = deal.clearanceTag;
  if (!tag) return null;

  const costs = dealCostBreakdown(deal);
  const original = Number(tag.originalPrice || 0);
  const clearance = Number(tag.clearancePrice || deal.ask || 0);
  const percentOff = Number(tag.percentOff || 0) || (original && clearance ? Math.round((1 - clearance / original) * 100) : 0);
  const hasPrice = clearance > 0;
  const hasIdentifier = Boolean(tag.upc || tag.sku || tag.styleNumber);
  const tone = !hasPrice && hasIdentifier ? "maybe" : costs.net >= 80 ? "good" : costs.net >= 25 ? "maybe" : "skip";
  const label = !hasPrice && hasIdentifier
    ? "Barcode found"
    : tone === "good"
      ? "Strong spread signal"
      : tone === "maybe"
        ? "Needs easy logistics"
        : "Weak spread signal";
  const note = !hasPrice && hasIdentifier
    ? "The photo showed a product/barcode label, but no price. Use the UPC/SKU for comps, then enter the shelf price before trusting the spread math."
    : tone === "good"
      ? "The clearance price may leave room after normal fees and packing."
      : tone === "maybe"
        ? "The tag is interesting, but fees, returns, or shipping could erase it."
        : "The quick math does not leave enough room yet.";

  return { ...tag, original, clearance, percentOff, net: costs.net, tone, label, note, hasPrice };
}

