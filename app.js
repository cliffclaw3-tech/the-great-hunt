function challengeTaskList() {
  const rows = challengeRows();
  const readyLeads = rows
    .filter(({ entry }) => ["watching", "contacted"].includes(entry.stage))
    .map(row => ({ ...row, readiness: quickFlipAssessment(row.find) }))
    .filter(({ readiness }) => readiness.tone === "good")
    .sort((a, b) => b.readiness.net - a.readiness.net)
    .slice(0, 5);
  const paperOnly = rows
    .filter(({ entry }) => ["watching", "contacted"].includes(entry.stage))
    .map(row => ({ ...row, readiness: quickFlipAssessment(row.find) }))
    .filter(({ readiness }) => readiness.tone === "maybe")
    .sort((a, b) => b.readiness.net - a.readiness.net)
    .slice(0, 5);
  const blocked = rows
    .filter(({ entry }) => !["passed", "sold"].includes(entry.stage))
    .map(row => ({ ...row, readiness: quickFlipAssessment(row.find) }))
    .filter(({ readiness }) => readiness.tone === "skip")
    .slice(0, 5);
  const listNext = rows
    .filter(({ entry }) => entry.stage === "bought")
    .sort((a, b) => paperTradeProfit(b) - paperTradeProfit(a))
    .slice(0, 5);
  const closeNext = rows
    .filter(({ entry }) => entry.stage === "listed")
    .sort((a, b) => paperTradeProfit(b) - paperTradeProfit(a))
    .slice(0, 5);

  return {
    readyLeads,
    paperOnly,
    blocked,
    listNext,
    closeNext
  };
}

