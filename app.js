async function updateSavedFind(id, patch) {
  const index = savedFinds.findIndex(find => String(find.id) === String(id));
  if (index === -1) return;

  savedFinds[index] = normalizeSavedFind({ ...savedFinds[index], ...patch, updatedAt: new Date().toISOString() });
  saveLocalFinds();
  renderFinds();

  try {
    const updatedFind = await apiRequest(`/api/finds/${id}`, {
      method: "PATCH",
      body: JSON.stringify(patch)
    });
    const latestIndex = savedFinds.findIndex(find => String(find.id) === String(id));
    if (latestIndex !== -1) {
      savedFinds[latestIndex] = normalizeSavedFind(updatedFind);
      saveLocalFinds();
      renderFinds();
    }
  } catch {
    saveLocalFinds();
  }
}

